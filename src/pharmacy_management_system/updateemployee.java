    private void searchbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbtnActionPerformed
        // TODO add your handling code here:
        int checkUserExist = 0;
        String username = usernamebox.getText();
        if(username.equals("")){
            JOptionPane.showMessageDialog(null, "Username field is required");
        } else{
            SimpleDateFormat dFormat = new SimpleDateFormat("dd-MM-yyyy");
            try{
                Connection con = connectionprovider.getcon();
                Statement st = con.createStatement();
                ResultSet rs = st.executeQuery("select * from app_user where user_name = '"+username+"'");
                while(rs.next()){
                    usernamebox.setEditable(false);
                    checkUserExist = 1;
                    namebox.setText(rs.getString("name"));
                    mobilenobox.setText(rs.getString("mobile_no"));
                    emailbox.setText(rs.getString("email"));
                    addressbox.setText(rs.getString("address"));
                    String dob = rs.getString("dob");
                    if(dob!= ""){
                        try{
                           datebox.setDate(dFormat.parse(dob));
                        }
                        catch(Exception e){ }
                    }
                    if(rs.getString("userRole").equals("Admin")) {
                        userrolebox.removeAllItems();
                        userrolebox.addItem("Admin");
                        userrolebox.addItem("Pharmacist");
                    }
                    else{
                        userrolebox.removeAllItems();                        
                        userrolebox.addItem("Pharmacist");
                        userrolebox.addItem("Admin");
                    }
                }
        }   catch(Exception e) {
                JOptionPane.showMessageDialog(null,e);
            }
            if(checkUserExist == 0){
                JOptionPane.showMessageDialog(null,"Username does not exist");
            }
        }

    }//GEN-LAST:event_searchbtnActionPerformed

