    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed
        // TODO add your handling code here:
        String userRole = (String) userrolebox.getSelectedItem();
        String name = namebox.getText();
        SimpleDateFormat dFormat = new SimpleDateFormat("dd-MM-yyyy");
        Date date = datebox.getDate();
        String dob ="";
        if(date != null){
            dob = dFormat.format(datebox.getDate());
        }
        String mobileNumber = mobilenobox.getText();
        String email = emailbox.getText();
        String username = usernamebox.getText();
        String address = addressbox.getText();
        if(name.equals("")){
            JOptionPane.showMessageDialog(null,"Name field is required.");
        } else if(dob.equals("")){
            JOptionPane.showMessageDialog(null,"Date of Birth field is required.");
        } else if(mobileNumber.equals("")){
            JOptionPane.showMessageDialog(null,"Mobile Number field is required.");
        } else if(!mobileNumber.matches(mobileNumberPattern) || mobileNumber.length() !=10){
            JOptionPane.showMessageDialog(null,"Mobile Number field is invalid.");
        } else if(!email.matches(emailPattern)){
            JOptionPane.showMessageDialog(null,"Email field is invalid.");
        } else if(email.equals("")){
            JOptionPane.showMessageDialog(null,"Email field is required.");
        } else if(username.equals("")){
            JOptionPane.showMessageDialog(null,"Username field is required.");
        } else if(address.equals("")){
            JOptionPane.showMessageDialog(null,"Address field is required");
        } else{
            try {
                            Connection con = connectionprovider.getcon();
                            PreparedStatement ps = con.prepareStatement("UPDATE app_user SET userRole=?, name=?, dob=?, mobile_no=?, email=?, address=? WHERE user_name=?");
    ps.setString(1, userRole);
    ps.setString(2, name);
    ps.setString(3, dob);
    ps.setString(4, mobileNumber);
    ps.setString(5, email);
    ps.setString(6, address);
    ps.setString(7, username);
    int rowsAffected = ps.executeUpdate(); 
    if (rowsAffected > 0) {
        JOptionPane.showMessageDialog(null, "User Updated Successfully");
        setVisible(false);
        new updateemployee().setVisible(true);
    } else {
        JOptionPane.showMessageDialog(null, "Update failed. No matching user found.");
    }
} catch (Exception e) {
    JOptionPane.showMessageDialog(null, e);
}

        }

    }//GEN-LAST:event_updatebtnActionPerformed

