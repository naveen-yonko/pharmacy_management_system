    private void searchbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbtnActionPerformed
        // TODO add your handling code here:
        int checkExist = 0;
        String medid = medidbox.getText();
        if(medid.equals("")){
            JOptionPane.showMessageDialog(null, "medicine ID field is required");
        } else{
            try{
                Connection con = connectionprovider.getcon();
                Statement st = con.createStatement();
                ResultSet rs = st.executeQuery("select * from medicines where unique_id = '"+medid+"'");
                while(rs.next()){
                    medidbox.setEditable(false);
                    checkExist = 1;
                    mednamebox.setText(rs.getString("medname"));
                    comnamebox.setText(rs.getString("companyname"));
                    unitpricebox.setText(rs.getString("amount"));
                    totquantbox.setText(rs.getString("quantity"));
                    addquantbox.setText("0");
                }
                if(checkExist ==0){
                    JOptionPane.showMessageDialog(null, "medicine doesn't exist");
                    
                }
            }
            catch(Exception e){
                
            }
        }
    }//GEN-LAST:event_searchbtnActionPerformed

