    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed
        // TODO add your handling code here:
                    String medname = mednamebox.getText();
                    String comname = comnamebox.getText();
                    String unit = unitpricebox.getText();
                    String totqua = totquantbox.getText();
                    String add = addquantbox.getText();
                    String medid = medidbox.getText();
                    int newstock = 0;
                    if(mednamebox.equals("")){
            JOptionPane.showMessageDialog(null,"Medicine Name field is required.");
        } else if(comnamebox.equals("")){
            JOptionPane.showMessageDialog(null,"Company Name field is required.");
        } else if(unitpricebox.equals("")){
            JOptionPane.showMessageDialog(null,"unitprice field is required.");
        } else if(totquantbox.equals("")){
            JOptionPane.showMessageDialog(null,"Total Quantity field is invalid.");
        }else if(addquantbox.equals("")){
            JOptionPane.showMessageDialog(null,"add quantity  field is required.");
        }else{
            try {
                newstock = Integer.parseInt(add) + Integer.parseInt(totqua);
                Connection con = connectionprovider.getcon();
                PreparedStatement ps = con.prepareStatement("UPDATE medicines SET medname=?, companyname=?, quantity = ? ,amount=? WHERE unique_id=?");
                ps.setString(1, medname);
                ps.setString(2, comname);
                ps.setInt(3, newstock);
                ps.setString(4, unit);
                ps.setString(5, medid);
                int rowsAffected = ps.executeUpdate(); 
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(null, "Medicine Details Updated Successfully");
                    setVisible(false);
                    new updatemedicine().setVisible(true);
                }
                else {
                    JOptionPane.showMessageDialog(null, "Update failed. No matching medicine found.");
                }
            }
            catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_updatebtnActionPerformed

